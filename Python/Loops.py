# Loop through a collection
# for language in ["HTML", "CSS", "JavaScript"]:
web_language = ["HTML", "CSS", "JavaScript"]
for language in web_language:
    print(language)

# Looping a number of times
for index in range(1, 11):
    print(index)

# Looping with a condition
language = ["Python", "Ruby", "Java"]
i = 0
while i < len(language):
    print(language[i])
    i = i + 1
